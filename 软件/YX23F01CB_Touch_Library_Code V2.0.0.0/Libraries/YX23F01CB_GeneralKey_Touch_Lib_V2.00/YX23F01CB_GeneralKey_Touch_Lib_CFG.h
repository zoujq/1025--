//-----------------------------------------------------------------│---------│----------------------------------------正噪声值				 
//      设置基线微调速度(0~255)                                    │         │Differ在±噪声之间时，内部噪声桶累加噪声，
#define HCTouchLib_NOISE_BARREL_CAPACITY                80      // │   III   │累加和的绝对值超过设置值后基线微调，在湿     
//                                                                 │         │度及温度变化较快时建议将其设置的更小
//-----------------------------------------------------------------│---------│----------------------------------------负噪声值
//      设置低基准线复位时间(0~255)                                │         │Differ<负噪声值是，内部计数器累加值超过区
#define HCTouchLib_OVERLOW_TIME                         100     // │   IV    │域IV设置值后，按键无效，更新基线	   																				
