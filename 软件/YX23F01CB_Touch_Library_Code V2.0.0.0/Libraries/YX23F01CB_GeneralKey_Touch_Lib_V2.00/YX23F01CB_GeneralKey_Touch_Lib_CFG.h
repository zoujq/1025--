//      区域I，区域II，区域IV 的更新时间 ≈ 设置值 × 一轮扫描时间   │         │
//      区域III的微调原理 ：│Differ累加值│ > 设置值 时 基线微调    │         │
//-----------------------------------------------------------------│---------│----------------------------------------触摸有效上限值
