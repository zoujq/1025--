#define HCTouchLib_NOISE_THRESHOLD 	{ 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 , 10 }

//      触摸阈值(25~65535)          至少为通道噪声值3倍以上,5倍以上更佳
#define HCTouchLib_FINGER_THRESHOLD	{ 120, 120, 120, 120, 120, 120, 120, 120, 120, 120, 120, 120, 120, 120, 120, 120}

//      触摸迟滞(5~50)              与触摸阈值配合使用，该值应略大于所设噪声阈值
#define HCTouchLib_HYSTERESIS                           15	

//      触摸有效上限值(0~255)       上限值 = HCTouchLib_LIMIT_FACTOR × 对应通道的触摸阈值，0表示不设置上限
#define HCTouchLib_LIMIT_FACTOR                         0

//      按键确认消抖次数(1~255)		按下消抖时间 = 单个按键扫描时间 × 开启的检测通道个数 × 按键确认消抖次数
#define HCTouchLib_CONFIRM_TOUCH_TIME                   2

//      按键离开消抖次数(1~255)		松开消抖时间 = 单个按键扫描时间 × 开启的检测通道个数 × 按键离开消抖次数
#define HCTouchLib_CONFIRM_NOTOUCH_TIME                 2
